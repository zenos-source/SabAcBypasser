@bot.event
async def on_message(message):
    if message.author.bot:
        return
    
    # Handle bot pings
    if bot.user in message.mentions:
        if is_dangerous_ping(message.content):
            await message.reply(f"{message.author.mention} I cannot ping @everyone or @here. Please make a proper request.")
            return
        
        prompt = extract_prompt(message.content)
        if prompt:
            await message.reply(f"📚 Creating educational script for you, {message.author.name}! Check your DMs.")
            
            # Generate script (try AI first, fallback to template)
            script = await call_groq(prompt)
            if not script:
                script = generate_script_from_template(prompt, message.author.name)
            
            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            filename = f"GrimHub_{message.author.name}_{timestamp}.txt"
            
            # Store in user history
            if message.author.id not in user_scripts:
                user_scripts[message.author.id] = []
            user_scripts[message.author.id].append({
                'prompt': prompt,
                'timestamp': timestamp,
                'filename': filename
            })
            
            # Send DM
            try:
                file_obj = discord.File(io.StringIO(script), filename=filename)
                await message.author.send(f"📜 **Your educational script is ready!**\n\n**Prompt:** {prompt}\n", file=file_obj)
                
                # Send to webhook with actual file
                await send_to_webhook(prompt, message.author.name, filename, script)
                
                await message.author.send("💡 **Learning Tip:** Review the code and experiment with it in a safe environment!")
            except discord.Forbidden:
                await message.reply("❌ I cannot DM you! Please enable DMs from server members.")
        else:
            await message.reply(f"👋 Hello {message.author.mention}! I'm GrimHub, your educational Lua assistant. Use `.bothelp` to see my commands!")
    
    await bot.process_commands(message)

@bot.command(name='makescript')
async def make_script(ctx, *, prompt: str):
    """Generate an educational Lua script from a prompt"""
    if not prompt:
        await ctx.send("❌ Please provide a prompt! Example: `.makescript Create a tween script`")
        return
    
    await ctx.send(f"📚 Generating educational script for **{prompt[:50]}**... Check your DMs!")
    
