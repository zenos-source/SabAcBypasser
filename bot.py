def is_dangerous_ping(content: str) -> bool:
    return '@everyone' in content or '@here' in content

def extract_prompt(content: str) -> str:
    # Remove bot mention
    prompt = re.sub(r'<@!?\d+>', '', content).strip()
    return prompt

async def call_groq(prompt: str, system_prompt: str = None) -> str:
    """Call Groq API for AI-powered script generation"""
    if not GROQ_API_KEY:
        return None
    
    default_system = """You are GrimHub, an educational Lua scripting assistant. 
    Generate clean, well-commented Lua code for Roblox. Focus on teaching concepts.
    Never generate anti-cheat bypasses or malicious code. Keep responses concise."""
    
    url = "https://api.groq.com/openai/v1/chat/completions"
    headers = {
        "Authorization": f"Bearer {GROQ_API_KEY}",
        "Content-Type": "application/json"
    }
    data = {
        "model": "llama3-8b-8192",
        "messages": [
            {"role": "system", "content": system_prompt or default_system},
            {"role": "user", "content": prompt}
        ],
        "temperature": 0.7,
        "max_tokens": 1500
    }
    
    try:
        async with aiohttp.ClientSession() as session:
            async with session.post(url, json=data, headers=headers, timeout=30) as resp:
                if resp.status == 200:
                    result = await resp.json()
                    return result["choices"][0]["message"]["content"]
    except Exception as e:
        print(f"Groq error: {e}")
    return None

